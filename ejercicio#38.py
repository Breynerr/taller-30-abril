#creciendo, decreciendo o ninguna
numero1 = int(input(" ingresen el numero1 = "))
numero2 = int(input(" ingresen el numero2 = "))
numero3 = int(input(" ingresen el numero3 = "))
if numero1<numero2<numero3:
    print("esta incrementando")
elif numero1>numero2>numero3:
    print("esta decreciendo")
else:
    print("ni incrementando ni decreciendo")

